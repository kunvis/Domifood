        <p id="descrip">La gestión de calidad comprende todas las acciones y procedimientos que 
            garantizan la excelencia en los productos y procesos. Aplicado al restaurante, 
            este concepto se refiere especialmente a la calidad del 
            servicio global, que se valora en función de las expectativas del cliente.</p>
